    public QuizExam(String rollno) {
        initComponents();
        setExtendedState(MAXIMIZED_BOTH);
        
        SimpleDateFormat dFormat=new SimpleDateFormat("dd-MM-yyyy");
        Date date=new Date();
        jLabel3.setText(dFormat.format(date));
        
        //first question and student details
        try
        {
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select *from student where rollno='"+rollno+"'");
            while(rs.next())
            {
                jLabel10.setText(rollno);
                jLabel12.setText(rs.getString(2));
            }
           ResultSet rs1=st.executeQuery("select *from question where id='"+id+"'");
           while(rs1.next())
           {
               jLabel16.setText(rs1.getString(1));
               jLabel19.setText(rs1.getString(2));
               jRadioButton1.setText(rs1.getString(3));
               jRadioButton2.setText(rs1.getString(4));
               jRadioButton3.setText(rs1.getString(5));
               jRadioButton4.setText(rs1.getString(6));
               answer=rs1.getString(7);
           }
            
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
        
        setLocationRelativeTo(this);
        time=new Timer(1000,new ActionListener(){
            public void actionPerformed(ActionEvent e)
        {
            jLabel8.setText(String.valueOf(sec));
            jLabel7.setText(String.valueOf(min));
            if(sec==60)
        {
            sec=0;
            min++;
            if(min==10)
        {
            time.stop();
            answerCheck();
            Submit();
        }
        }
            sec++;
        }
        
        });
        time.start();
    }


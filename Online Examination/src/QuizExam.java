    public void answerCheck()
    {
        String studentanswer="null";
        if(jRadioButton1.isSelected())
        {
            studentanswer=jRadioButton1.getText();
        }
        else if(jRadioButton2.isSelected())
                {
                   studentanswer=jRadioButton2.getText();
                }
        else if(jRadioButton3.isSelected())
                {
                   studentanswer=jRadioButton3.getText();
                }
        else 
                {
                   studentanswer=jRadioButton4.getText();
                }
        if(studentanswer.equals(answer))
        {
            marks=marks+1;
            String marks1=String.valueOf(marks);
            jLabel18.setText(marks1);
        }
        
        //question  number change
        int id1=Integer.parseInt(id);
        id1=id1+1;
        id=String.valueOf(id1);
        
        //clear jRadioButton
        
        jRadioButton1.setSelected(false);
        jRadioButton2.setSelected(false);
        jRadioButton3.setSelected(false);
        jRadioButton4.setSelected(false);
        
        //last que
        if(id.equals("10"))
        {
            jButton2.setVisible(false);
        }
    }

    public void Submit()
    {
        String rollNo=jLabel10.getText();
        answerCheck();
        try
        {
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            st.executeUpdate("update student set marks='"+marks+"' where rollNo='"+rollNo+"'");
            String marks1=String.valueOf(marks);
            setVisible(false);
            new SuccessfullySubmited(marks1).setVisible(true);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }
    public void question()
    {
        try
        {
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            
           ResultSet rs1=st.executeQuery("select *from question where id='"+id+"'");
           while(rs1.next())
           {
               jLabel16.setText(rs1.getString(1));
               jLabel19.setText(rs1.getString(2));
               jRadioButton1.setText(rs1.getString(3));
               jRadioButton2.setText(rs1.getString(4));
               jRadioButton3.setText(rs1.getString(5));
               jRadioButton4.setText(rs1.getString(6));
               answer=rs1.getString(7);
           }
            
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
        
    }
